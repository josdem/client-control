	private JCheckBox getAcceptConditionsCheckBox() {
		if (acceptConditionsCheckBox == null) {
			acceptConditionsCheckBox = new JCheckBox();
			acceptConditionsCheckBox.setBounds(ACCEPT_CONDITION_CHECKBOX_BOUNDS);
			acceptConditionsCheckBox.setName(ACCEPT_CONDITIONS_CHECK_BOX_NORMAL_NAME);
			acceptConditionsCheckBox.setForeground(SynthColors.GRAY909090);
			acceptConditionsCheckBox.setIconTextGap(ICON_TEXT_GAP);

			acceptConditionsCheckBox.addActionListener(new ActionListener() {
				@Override
				public void actionPerformed(ActionEvent e) {
					if (acceptConditionsCheckBox.isSelected()) {
						acceptConditionsCheckBox.setSelected(false);
					} else {
						acceptConditionsCheckBox.setSelected(true);
						validateAllUserData();
					}
					showFields(false);
					getDisclaimerPanel().setVisible(true);
					enableForm(false);
				}

			});

			acceptConditionsCheckBox.addFocusListener(new FocusListener() {

				@Override
				public void focusLost(FocusEvent e) {
					acceptConditionsCheckBox.setName(ACCEPT_CONDITIONS_CHECK_BOX_NORMAL_NAME);
					acceptConditionsCheckBox.setForeground(SynthColors.GRAY909090);
				}

				@Override
				public void focusGained(FocusEvent e) {
					acceptConditionsCheckBox.setName(ACCEPT_CONDITIONS_CHECK_BOX_FOCUS_NAME);
					acceptConditionsCheckBox.setForeground(SynthColors.GRAY909090);
				}
			});
		}
		return acceptConditionsCheckBox;
	}

