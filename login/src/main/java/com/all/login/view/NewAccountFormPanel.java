	private JPasswordField getPasswordTextField() {
		if (passwordTextField == null) {
			passwordTextField = new JPasswordField();
			passwordTextField.setBounds(PASSWORD_TEXTFIELD_BOUNDS);
			passwordTextField.setSelectionColor(COLOR_SELECTION_TEXTFIELDS);
			passwordTextField.setName(PASSWORD_TEXT_FIELD_NAME);
			passwordTextField.setEchoChar('\u25CF');
			passwordTextField.addKeyListener(new KeyAdapter() {
				@Override
				public void keyReleased(KeyEvent e) {
					validatePasswordStrength();
					String password = new String(passwordTextField.getPassword());
					getUser().setPassword(password);
					boolean validSize = passwordFieldValidator.isPasswordSizeAllowed(password);
					passwordFieldValidator.passwordRenameValidate(passwordTextField, PASSWORD_TEXT_FIELD_NAME, validSize,
							messages);
					validateAllUserData();
				}
			});
			passwordTextField.addKeyListener(new DisableCopyPasteKey());
			passwordTextField.setDocument(new JTextFieldLimit(RegisterUserCommand.MAXIMUM_PASSWORD_SIZE));
		}
		return passwordTextField;
	}

