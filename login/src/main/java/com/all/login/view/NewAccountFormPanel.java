	class KeyValidator extends KeyAdapter {
		private JTextField field;
		private String propertyName;
		private String originalName;
		private final boolean capitalized;

		public KeyValidator(JTextField field, String propertyName, boolean capitalized) {
			super();
			this.capitalized = capitalized;
			this.propertyName = propertyName;
			this.field = field;
			this.originalName = field.getName();
		}

		@Override
		public void keyReleased(KeyEvent e) {
			int caretPosition = field.getCaretPosition();
			Set<ConstraintViolation<RegisterUserCommand>> violations;
			if (this.capitalized) {
				capitalizeText(field);
			}
			violations = invokeMethod(propertyName, field.getText());
			if (violations.size() > 0) {
				showViolationMessage(field, propertyName, violations);
			} else {
				settingsForRightText(field, originalName);
			}
			validateAllUserData();
			field.setCaretPosition(caretPosition);
		}
	}

